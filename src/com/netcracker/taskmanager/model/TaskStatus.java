package com.netcracker.taskmanager.model;

/**
 * Class of enums describes status of task
 */
public enum TaskStatus{
    /**
     * Free status
     */
    FREE,
    /**
     * Occupied status
     */
    OCCUPIED,
    /**
     * Started status
     */
    STARTED,
    /**
     * Status in progress
     */
    IN_PROGRESS,
    /**
     * Waiting status
     */
    WAITING,
    /**
     * Delayed status
     */
    DELAYED,
    /**
     * Finished status
     */
    FINISHED,
}
